import { createPublicClient, createWalletClient, http, PrivateKeyAccount, SimulateContractReturnType } from 'viem';
import { printError, printInfo, printSuccess } from '../../../data/logger/logPrinter';
import { checkGwei } from '../../../data/helpers/gweiChecker';
import { linea } from 'viem/chains';
import { Config } from '../../../config';
import { addTextMessage } from '../../../data/telegram/telegramBot';
import { gamerBoomContractAddress, gamerBoomProofContractAddress } from './gamerBoomData';
import { gamerBoomABI } from '../../../abis/gamerBoom';
import { genesisBoomABI } from '../../../abis/gamerBoomProof';

export async function signGenesisProof(account: PrivateKeyAccount) {
    printInfo(`Выполняю модуль Week1 - Gamer Boom - Sign Genesis Proof. GamerBoom: Genesis Testing with Linea`);

    await checkGwei();

    const lineaClient = createPublicClient({
        chain: linea,
        transport: Config.rpc.url == '' ? http() : http(Config.rpc.url),
    });

    printInfo(`Вызываю функцию Sign Genesis Proof`);

    const walletClient = createWalletClient({
        chain: linea,
        transport: Config.rpc.url == '' ? http() : http(Config.rpc.url),
    });

    const nonce = await lineaClient.getTransactionCount({ address: account.address });
    const transferRequest = await lineaClient
        .simulateContract({
            address: gamerBoomProofContractAddress,
            abi: genesisBoomABI,
            functionName: 'signGenesisProof',
            account: account,
            nonce: nonce,
        })
        .then((result) => result as SimulateContractReturnType)
        .catch((e) => {
            printError(`Произошла ошибка во время выполнения модуля Sign Genesis Proof - ${e}`);
            return { transferRequest: undefined };
        });

    if (transferRequest !== undefined && 'request' in transferRequest) {
        const hash = await walletClient.writeContract(transferRequest!.request).catch((e) => {
            printError(`Произошла ошибка во время выполнения модуля Sign Genesis Proof - ${e}`);
            return false;
        });

        if (hash == false) {
            return false;
        }

        const url = `${linea.blockExplorers?.default.url + '/tx/' + hash}`;

        printSuccess(`Транзакция успешно отправлена. Хэш транзакции: ${url}\n`);

        await addTextMessage(
            `✅Week1 - Sign Genesis Proof: GamerBoom: Genesis Testing with Linea <a href='${url}'>link</a>`,
        );
    }

    return true;
}

